<p>Jamie Edward Lenman (born 9 November 1982) is an English musician and illustrator. He was the lead singer, guitarist and main songwriter for British alternative rock trio Reuben, from 2001 to 2008 whilst also working as an illustrator, which upon Reuben disbanding he made his primary focus. In 2013, Lenman began releasing and performing as a solo artist and has gone on to release four albums, one being a covers album titled Shuffle in 2019.

As an illustrator, Lenman worked full-time for The Guardian from 2007 until 2014. He has also frequently contributed to the Doctor Who Magazine under the pseudonym ‘Baxter’, releasing a collection of his work so far for the magazine called ‘Whoah!’ in 2014.</p>
